var RIOT = function(hook){
    var pa,sa,pb,sb;
    var PORTA, PORTB;
    var timer=0;
    var divisor = 1;
    var oldDivisor=1;
    var act = 1;
    var inte=0;
    var drop=0;

    var doTick=function() {
        act--;
        if (act==0) {
            act=divisor;
            timer--;
            if (timer<0) {
                timer=255;
                //overflow
                divisor=1;
                drop=1; //overflow
            }
        }
    };

    return {

        tick: function (Ts) {
            for(var i=0;i<Ts;i++) {
                doTick();
            }
        },

        byteTo : function(addr,b) {
            //console.log("RIOT w",addr,b);
            switch(addr) {
                case 0: sa = b;break;
                case 1: pa = b;break;
                case 2: sb = b;break;
                case 3: pb = b;break;
                case 4: divisor=1; oldDivisor=1;timer = b; drop=0; inte=0;break;
                case 5: divisor=8; oldDivisor=8;timer = b; drop=0; inte=0;break;
                case 6: divisor=64; oldDivisor=64;timer = b; drop=0; inte=0;break;
                case 7: divisor=1024; oldDivisor=1024;timer = b; drop=0; inte=0;break;
                case 0xc: divisor=1; oldDivisor=1;timer = b; drop=0; inte=1;break;
                case 0xd: divisor=8; oldDivisor=8;timer = b; drop=0; inte=1;break;
                case 0xe: divisor=64; oldDivisor=64;timer = b; drop=0; inte=1;break;
                case 0xf: divisor=1024; oldDivisor=1024;timer = b; drop=0; inte=1;break;
                default: console.log('CLK not implemented');
            }

            hook(sa,sb,pa,pb);
            
        },
        byteAt : function(addr) {
            //console.log("RIOT r",addr);
            switch(addr) {
                case 0: return (pa & sa) | ((~pa&0xff) & PORTA);
                case 1: return pa;
                case 2: return (pb & sb) | ((~pb&0xff) & PORTB);
                case 3: return pb;
                case 6: inte=0; if (drop) {drop=0;divisor=oldDivisor;} return timer;
                case 0xe: inte=1; if (drop) {drop=0;divisor=oldDivisor;} return timer;
                case 7: return (drop==0)?0:0x80;
                default: ;
            }           
            return 0; 
        },
        portA: function (value) {
            PORTA = value;
        },
        portB: function (value) {
            PORTB = value;
        }
    }
};


