function mgi(T) {
  if (!mgplayback) return 0x80;
  if (mgpt == 0) {
    mgpt = T;
  }
  var diff = T - mgpt;
  mgdelay -= diff;
  mgpt = T;
  if (mgdelay > 0) return mgplast;
  mgdelay = mgf[mgpos++];
  mgplast ^= 0x80;
  if (mgpos % 50 == 0) $('#mginfo').html("PLAYBACK " + (mgf.length - mgpos));
  if (mgpos > mgf.length) mgplayback = false;
  //console.log(mgplast);
  return mgplast;

};

