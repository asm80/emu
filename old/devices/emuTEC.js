function mgo(PA, T) {
  if (!mgrec) return;
  if (mgwait && ((PA & 0x80) > 0)) {
    //go!
    mgt = T;
    mgwait = false;
    mglast = 0x80;
    return;
  }

  var mgnew = PA & 0x80;
  if (mgnew != mglast) {
    mgf.push(T - mgt);
    if (mgf.length % 50 == 0) $('#mginfo').html("RECORDING " + mgf.length);
    mglast = mgnew;
    mgt = T;
  }
};

